    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jComboBox1.equals("Escolha um dos vídeos...")){
            JOptionPane.showMessageDialog(rootPane, "Ocorreu algúm erro");
        }else{
            for(int i=0; i<listav.size(); i++){
                if(jComboBox1.getSelectedItem().equals("Nome vídeo: " + listav.get(i).getNome_video() + "Dono: " + listav.get(i).getNome_dono() + "Url: " + listav.get(i).getUrl_video())){
                    VideoDAO vd = new VideoDAO();
                    vd.inserirNota(Integer.parseInt(jTNota.getText()), listav.get(i).getId());
                     JOptionPane.showMessageDialog(rootPane, "Nota lançada com sucesso!");
                     
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

