    public void CarregarLista(){
        VideoDAO vd = new VideoDAO();
        List<Video> lista = vd.listaDeVideos();
        listav = vd.listaDeVideos();
        for(int i=0;i<lista.size();i++){
            jComboBox1.addItem("Nome vídeo: " + lista.get(i).getNome_video() + "Dono: " + lista.get(i).getNome_dono() + "Url: " + lista.get(i).getUrl_video());
        }
    }
